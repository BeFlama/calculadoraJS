class Calculadora {
    sumar(num1, num2){
        return num1 + num2;
    }
/* un file con class como este de js se inicia con mayusucla en los archivos */
    restar(num1, num2){
        return num1 - num2;
    }
    dividir(num1, num2){
        return num1 / num2;
    }
    multiplicar(num1, num2){
        return num1 * num2;
    }
}